import argparse            # Модуль для парсинга аргументов командной строки
import random              # Модуль для генерации случайных чисел

# Создаём парсер аргументов
parser = argparse.ArgumentParser()  # Объект, который будет разбирать аргументы командной строки
parser.add_argument("N", type=int, nargs='?', help="Количество чисел")  # Добавляем аргумент N
args = parser.parse_args()  # Разбираем введённые аргументы (args.N теперь либо число, либо None)

# Если аргумент N не передали при запуске, спрашиваем у пользователя
if args.N is None:
    args.N = int(input("Введите количество чисел N: "))  # Вводим вручную и преобразуем в int

# Создаём список случайных чисел от 0 до 1 длиной N
list_1 = [random.random() for _ in range(args.N)]

# Пузырьковая сортировка: внешний цикл — количество проходов
for _ in range(len(list_1)):
    # Внутренний цикл — сравнение соседних элементов
    for j in range(len(list_1) - 1):
        # Если текущий элемент больше следующего — меняем местами
        if list_1[j + 1] < list_1[j]:
            list_1[j], list_1[j + 1] = list_1[j + 1], list_1[j]


print(list_1)
