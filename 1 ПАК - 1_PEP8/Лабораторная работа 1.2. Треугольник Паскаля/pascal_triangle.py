import argparse  # Модуль для разбора аргументов командной строки

# Создаём объект парсера аргументов
parser = argparse.ArgumentParser()

# Добавляем позиционный аргумент "h" — высота треугольника (количество строк),
# по умолчанию спрашиваем у пользователя, если не указано
parser.add_argument("h", type=int, nargs='?', help="Высота треугольника")

# Разбираем введённые аргументы (args.h теперь либо число, либо None)
args = parser.parse_args()

# Если аргумент h не передали при запуске, спрашиваем у пользователя
if args.h is None:
    args.h = int(input("Введите высоту треугольника h: "))  # Вводим вручную и преобразуем в int

# Создаём пустой список, в котором будем хранить треугольник Паскаля
P = []

# Основной цикл по строкам треугольника: i — номер строки (с нуля)
for i in range(args.h):
    # Создаём новую строку из единиц длиной i+1 (первый и последний элемент всегда 1)
    row = [1] * (i + 1)

    # Проходим по всем элементам строки, кроме краевых
    for j in range(1, i):  # внутренняя часть (без первых и последних)
        row[j] = P[i - 1][j - 1] + P[i - 1][j]

    # Добавляем готовую строку в общий список треугольника
    P.append(row)

# Выводим треугольник Паскаля на экран в виде равнобедренного треугольника
for i, r in enumerate(P):
    # Считаем количество пробелов перед строкой:
    # высота треугольника - номер текущей строки - 1
    print(' ' * (args.h - i - 1), end='')
    # Выводим элементы строки через пробел
    print(' '.join(map(str, r)))


